const quilljsCall = async () => {
  const link = document.createElement('link')
  link.href = 'https://cdn.quilljs.com/1.3.6/quill.snow.css'
  link.rel = 'stylesheet'
  document.head.appendChild(link)

  // JS Î°úÎìú
  const script = document.createElement('script')
  script.src = 'https://cdn.quilljs.com/1.3.6/quill.min.js'
  script.onload = () => {
    quill = new window.Quill(quillEditor.value, {
      theme: 'snow',
      modules: {
        toolbar: [
          [{ 'header': [1, 2, 3, false] }],
          ['bold', 'italic', 'underline'],
          [{ 'list': 'ordered' }, { 'list': 'bullet' }],
          ['link', 'image'],
          ['link'],
          ['clean']
        ]
      }
    })

    // üî• ÌïµÏã¨: ÎÇ¥Ïö© Î≥ÄÍ≤Ω Í∞êÏßÄ Ïù¥Î≤§Ìä∏ Ï∂îÍ∞Ä
    quill.on('text-change', () => {
      detail.value.memo = quill.root.innerHTML
    })

    // Ïù¥ÎØ∏ÏßÄ Ìï∏Îì§Îü¨ Ïª§Ïä§ÌÑ∞ÎßàÏù¥Ïßï
    quill.getModule('toolbar').addHandler('image', () => {
      const input = document.createElement('input')
      input.setAttribute('type', 'file')
      input.setAttribute('accept', 'image/*')
      input.click()

      input.onchange = () => {
        const file = input.files[0]
        if (file) {
          const reader = new FileReader()
          reader.onload = (e) => {
            const range = quill.getSelection()
            quill.insertEmbed(range.index, 'image', e.target.result)
            // üî• Ïù¥ÎØ∏ÏßÄ ÏÇΩÏûÖ ÌõÑ content ÏóÖÎç∞Ïù¥Ìä∏
            detail.value.memo = quill.root.innerHTML
          }
          reader.readAsDataURL(file)
        }
      }
    })

    // Í∏∞Ï°¥ ÎÇ¥Ïö©Ïù¥ ÏûàÎã§Î©¥ Î°úÎìú
    if (detail.value.memo) {
      quill.root.innerHTML = detail.value.memo
    }
  }
  document.head.appendChild(script)
}


const fetchDetail = async (data) => {
  loading.value = true
  try {
    const response = await axios.post('/store/detail', {
      storeCode: data
    })


    Object.assign(detail.value, response.data.data)
    detail.value.categoryType = detail.value.categoryType.split(',').map(item => parseInt(item.trim()))
    console.log(detail.value.products)

    setInitialFlags()
  } catch (error) {
    console.error('ÏÉÅÏÑ∏ Ï°∞Ìöå ÏóêÎü¨:', error)
  } finally {
    loading.value = false;
  }
}

