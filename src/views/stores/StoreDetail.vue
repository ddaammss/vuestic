const addProduct = () => {
  detail.products.push({ name: '', price: '' })
}

const removeProduct = (index) => {
  if (detail.products.length > 1) {
    detail.products.splice(index, 1)
  }
}
//------------------------------------------------------------------------------------------------- Ïù¥ÎØ∏ÏßÄ Ï≤òÎ¶¨ Ìï®Ïàò
const handleFileSelect = (event) => {
  const files = Array.from(event.target.files)

  files.forEach(file => {
    if (file.type.startsWith('image/')) {
      const reader = new FileReader()
      reader.onload = (e) => {
        selectedImages.value.push({
          file: file,
          name: file.name,
          url: e.target.result
        })
      }
      reader.readAsDataURL(file)
    }
  })
}

// Ïù¥ÎØ∏ÏßÄ Ï†úÍ±∞
const removeImage = (index) => {
  selectedImages.value.splice(index, 1)
}

const save = async () => {

  try {
    if (!detail.storeName) {
      alert('ÏûÖÏ†êÏÇ¨Î•º ÏûÖÎ†•Ìï¥Ï£ºÏÑ∏Ïöî.')
      return;
    }

    const products = detail.products.filter(product =>
      product.name.trim() !== '' && product.price !== ''
    )

    if (products.length === 0) {
      alert('ÏÉÅÌíàÏùÄ 1Í∞ú Ïù¥ÏÉÅ Îì±Î°ùÌï¥ÏïºÌï©ÎãàÎã§.')
      return
    }
    if(selectedImages.value.length === 0){
      alert('Ïù¥ÎØ∏ÏßÄÎäî 1Í∞ú Ïù¥ÏÉÅ Îì±Î°ùÌï¥ÏïºÌï©ÎãàÎã§.')
      return
    }

    let imageArray = [];
    const formData = new FormData();
    if (selectedImages.value.length > 0) {
      selectedImages.value.forEach((item, index) => {
        const actualFile = item.file || item;
        if (actualFile instanceof File) {
          formData.append('images', actualFile);
        }else{
          formData.append('dbImages', actualFile);
        }
      });
      formData.append('type', 'store');
      formData.append('parentSeq', detail.seq);

      // ÏÑúÎ≤ÑÎ°ú Ï†ÑÏÜ°
      try {
        const uploadResponse = await axios.post('/common/upload/images', formData, {
          headers: { 'Content-Type': 'multipart/form-data' }
        });
        imageArray = uploadResponse.data.imagePaths; // ÏÑúÎ≤ÑÏóêÏÑú Î∞òÌôòÌïú Í≤ΩÎ°úÎì§
      } catch (error) {
        console.error('ÏóÖÎ°úÎìú Ïã§Ìå®:', error.response?.data);
      }
    }
    const saveData = {
      ...detail,
      categoryType: detail.categoryType.join(','),
      seq: detail.seq,
      storeCode : detail.storeCode,
      images: imageArray,
      products: products,
    }

    //console.log('Ï†ÄÏû•Ìï† Îç∞Ïù¥ÌÑ∞:', saveData)
    loading.value = true
    const response = await axios.post('/store/upsert', saveData)
    if (response.data.code === 200) {
      alert('Ï†ÄÏû•ÎêòÏóàÏäµÎãàÎã§.')
      goBack()
    } else {
      alert(response.data.message);
    }
  } catch (error) {
    console.error('Ï†ÄÏû• ÏóêÎü¨:', error)
    alert('Ï†ÄÏû• Ï§ë Ïò§Î•òÍ∞Ä Î∞úÏÉùÌñàÏäµÎãàÎã§.')
  } finally{
    loading.value = false;
  }
}

const goBack = () => {
  const searchData = {
    name: route.query.name,
    address: route.query.address,
    startDate: route.query.startDate,
    endDate: route.query.endDate,
    categoryType: route.query.categoryType
  }

  router.push({
    path: '/stores/list',
    query: searchData
  })
}

watch(categoryFlags, () => {
  detail.categoryType = []
  Object.keys(categoryFlags).forEach((key, index) => {
    if (categoryFlags[key]) {
      detail.categoryType.push(index)
    }
  })
}, { deep: true })

</script>

